--DDL to define all tables, keys, and relationships

--Locations Table
CREATE TABLE Locations (
    LocationID INT IDENTITY(100000,1) PRIMARY KEY,
    Address1 VARCHAR(100),
    Address2 VARCHAR(25),
    City VARCHAR(50),
    Province VARCHAR(50),
    ZIP VARCHAR(10),
    Country VARCHAR(60),
    Continent VARCHAR(13)
);
GO

--Department Table
CREATE TABLE Department (
    DepartID INT IDENTITY(11000,1) PRIMARY KEY,
    ManagerID INT REFERENCES Employee(EmployeeID),
    DepartName VARCHAR(25),
);
GO

--CustomerType Table
CREATE TABLE CustomerType (
    CuTypeID INT IDENTITY(12000,10) PRIMARY KEY,
    CuType VARCHAR(25)
);
GO

--Company Table
CREATE TABLE Company (
    CompanyID INT IDENTITY(5000,1) PRIMARY KEY,
    CompanyName VARCHAR(25),
    YearEst DATE,
    LocationID INT REFERENCES Locations(LocationID)
);
GO

--Employee Table
CREATE TABLE Employee (
    EmployeeID INT IDENTITY(11100,1) PRIMARY KEY,
    EmpFName VARCHAR(20),
    EmpLName VARCHAR(20),
    DOB DATE,
    MobileNum CHAR(10),
    Landline CHAR(10),
    Ext VARCHAR(4),
    DepartID INT REFERENCES Department(DepartID),
    HireDate DATE,
    ExitDate DATE
);
GO

--Customer Table
CREATE TABLE Customer (
    CustomerID INT IDENTITY(1,1) PRIMARY KEY,
    CustFName VARCHAR(20),
    CustLName VARCHAR(20),
    Gender CHAR(1),
    DOB DATE,
    Ethnicity VARCHAR(15),
    MobileNum CHAR(10),
    Email VARCHAR(100),
    LocationID INT REFERENCES Locations(LocationID),
    CuOwner INT REFERENCES Employee(EmployeeID),
    CuType INT REFERENCES CustomerType(CuTypeID),
    CompanyID INT REFERENCES Company(CompanyID)
);
GO

--Task Table
CREATE TABLE Task (
    TaskID INT IDENTITY (13000,1) PRIMARY KEY,
    ContactMode VARCHAR(20),
    Memo VARCHAR(500),
    StartTime DATETIME,
    EndTime DATETIME,
    EmpID INT REFERENCES Employee(EmployeeID),
    CustomerID INT REFERENCES Customer(CustomerID)
);
GO

--Sale Table
CREATE TABLE Sale (
    SaleID INT IDENTITY(200000,1) PRIMARY KEY,
    CustomerID INT REFERENCES Customer(CustomerID),
    SaleDate DATE
);
GO

--Product Table
CREATE TABLE Product(
    ProductID INT IDENTITY(10000,1) PRIMARY KEY,
    ProductName VARCHAR(20),
    ProductColor VARCHAR(30),
    ProductPrice MONEY
);
GO

--SalesProducts Table
CREATE TABLE SalesProducts (
    SaleID INT REFERENCES Sale(SaleID),
    ProductID INT REFERENCES Product(ProductID),
    PRIMARY KEY (SaleID, ProductID)
);
GO

